package homework;

public class Employee {
    private String Name;
    private String Type;
    private int Sum;

    public void setName(String name) {
        Name = name;
    }

    public void setType(String type) {
        Type = type;
    }

    public void setSum(int sum) {
        Sum = sum;
    }

    public String getName() {
        return Name;
    }

    public String getType() {
        return Type;
    }

    public int getSum() {
        return Sum;
    }
}
